void text_putc(int x, int y, char c, enum vga_color color) {
    for (int i = 0; i < 4; i++) {
        for (int j = 0; j < 8; j++) {
            vga_putpixel(x - i + 4, y - j + 8, color * bitget(font[(c * 4 + j/2)], ((j * 4) + i)));
        }
    }
}